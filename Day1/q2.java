package Day1;

import java.util.*;

// multiplication of table of given no.
public class q2 {
    public static void main(String args[]){
        Scanner sc = new Scanner(System.in);
        
        int n = sc.nextInt();
        for (int i=1; i<=10; i++){
            System.out.println(n*i);
        }
        sc.close();


    }
    
}
